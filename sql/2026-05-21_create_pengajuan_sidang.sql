CREATE TABLE pengajuan_sidang (
  id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  pengajuan_judul_id BIGINT UNSIGNED NOT NULL,
  status ENUM('DRAFT','SUBMITTED','NEED_REVISION','VERIFIED','REJECTED') NOT NULL DEFAULT 'DRAFT',
  catatan_sekretariat TEXT NULL,
  verified_by_user_id BIGINT UNSIGNED NULL,
  submitted_at DATETIME NULL,
  verified_at DATETIME NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (pengajuan_judul_id) REFERENCES pengajuan_judul(id)
);

